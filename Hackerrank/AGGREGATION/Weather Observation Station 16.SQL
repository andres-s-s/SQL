WHERE LAT_N > 38.7780 ;



--MySQL 
SELECT ROUND( LAT_N , 4 ) FROM STATION 
WHERE LAT_N > 38.7780 ORDER BY LAT_N ASC LIMIT 1;



--MS SQL Server
SELECT TOP 1 CAST( LAT_N AS DECIMAL(8,4) ) FROM STATION 
WHERE LAT_N > 38.7780 ORDER BY LAT_N ASC;
